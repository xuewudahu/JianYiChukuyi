package com.rq.net.bean;

public class GoodsTypeBean
{
    private int id;
    private String name;
    private String unit;
    
    public int getId() {
        return this.id;
    }
    
    public String getName() {
        return this.name;
    }
    
    public String getUnit() {
        return this.unit;
    }
    
    public void setId(final int id) {
        this.id = id;
    }
    
    public void setName(final String name) {
        this.name = name;
    }
    
    public void setUnit(final String unit) {
        this.unit = unit;
    }
}
